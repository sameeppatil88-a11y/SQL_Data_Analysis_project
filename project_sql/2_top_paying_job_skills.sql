/*
Question: What skills are required for the top-paying data analyst jobs?
- Use the top 10 highest-paying Data Analyst jobs from first query
- Add the specific skills required for these roles
- Why? It provides a detailed look at which high-paying jobs demand certain skills,
   helping job seekers understand which skills to develop that align with top salaries
*/

WITH top_paying_jobs AS ( -- WE make a CTE and then use it on the remaining tables to get skills



  SELECT
        jpf.job_id,
        jpf.job_title,
        jpf.job_location,
        jpf.job_schedule_type,
        jpf. salary_year_avg,
        jpf. job_posted_date,
        cd.name AS company_name

        FROM
        job_postings_fact AS jpf

        LEFT JOIN company_dim AS cd ON
        cd.company_id = jpf.company_id

        WHERE job_title_short = 'Data Analyst' AND 
        job_location = 'Anywhere' AND
        salary_year_avg IS NOT NULL

        ORDER BY salary_year_avg DESC
        LIMIT 10
        )

SELECT 
top_paying_jobs.*, -- As we need all the columns
skills

FROM top_paying_jobs -- This would be our A table since we do not care about that jobs that are not assocaited with any skills

INNER JOIN skills_job_dim ON  --We used two joins here one to fetch skill_id and other for skill names
top_paying_jobs.job_id = skills_job_dim.job_id

INNER JOIN skills_dim ON
skills_job_dim.skill_id = skills_dim.skill_id

ORDER BY salary_year_avg DESC



